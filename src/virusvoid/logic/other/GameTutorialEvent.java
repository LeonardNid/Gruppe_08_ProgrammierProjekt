package virusvoid.logic.other;

/**
 * Enum representing different game/tutorial events or actions.
 */
public enum GameTutorialEvent {

    /**
     * Event for moving the orbital boss projectiles.
     */
    MOVE_BOSS_ORBITAL_PROJECTILES,

    /**
     * Event for moving the homing boss projectiles.
     */
    MOVE_BOSS_HOMING_PROJECTILES,

    /**
     * Event for spawning player projectiles.
     */
    SPAWN_PLAYER_PROJECTILES,

    /**
     * Event for moving player projectiles.
     */
    MOVE_PLAYER_PROJECTILES,

    /**
     * Event for spawning enemy projectiles.
     */
    SPAWN_ENEMY_PROJECTILES,

    /**
     * Event for spawning boss projectiles.
     */
    SPAWN_BOSS_PROJECTILES,

    /**
     * Event for moving enemy projectiles.
     */
    MOVE_ENEMY_PROJECTILES,

    /**
     * Event for moving shooting enemies.
     */
    MOVE_SHOOTING_ENEMIES,

    /**
     * Event for moving default enemies.
     */
    MOVE_DEFAULT_ENEMIES,

    /**
     * Event for spawning a shooting enemy.
     */
    SPAWN_SHOOTING_ENEMY,

    /**
     * Event for spawning a default enemy.
     */
    SPAWN_DEFAULT_ENEMY,

    /**
     * Event for moving the boss.
     */
    MOVE_BOSS
}